CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR,
    login VARCHAR,
    name VARCHAR,
    birthday DATE
);

CREATE TABLE IF NOT EXISTS MPAs (
     id BIGINT PRIMARY KEY,
     name VARCHAR
);

CREATE TABLE IF NOT EXISTS films (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR,
    description VARCHAR,
    release_date DATE,
    duration INTEGER,
    MPA_id BIGINT REFERENCES MPAs
);

CREATE TABLE IF NOT EXISTS friends (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_id BIGINT REFERENCES users,
    second_id BIGINT REFERENCES users
);

CREATE TABLE IF NOT EXISTS likes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    film_id BIGINT REFERENCES films,
    user_id BIGINT REFERENCES users
);

CREATE TABLE IF NOT EXISTS genres (
    id BIGINT PRIMARY KEY,
    name VARCHAR
);

CREATE TABLE IF NOT EXISTS film_genres (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    film_id BIGINT REFERENCES films,
    genre_id BIGINT REFERENCES genres
);

CREATE TABLE IF NOT EXISTS reviews (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content VARCHAR,
    is_positive BOOLEAN,
    user_id BIGINT REFERENCES users,
    film_id BIGINT REFERENCES films,
    useful INTEGER
);

CREATE TABLE IF NOT EXISTS reviews_ratings (
    review_id BIGINT REFERENCES reviews,
    user_id BIGINT REFERENCES users,
    status BOOLEAN --true: like; false: dislike
);

CREATE TABLE IF NOT EXISTS events (
                        event_id     BIGSERIAL PRIMARY KEY,
                        timestamp    BIGINT NOT NULL,
                        user_id      BIGINT REFERENCES users(id),
                        event_type   VARCHAR(10) NOT NULL,
                        operation    VARCHAR(10) NOT NULL,
                        entity_id    BIGINT NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_events_user_timestamp
    ON events(user_id, timestamp DESC);